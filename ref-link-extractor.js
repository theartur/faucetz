function requestData (item, done) {
    var url = require('url').parse(item.link);
    
    require('http').get({
        host: url.host,
        path: url.path
    }, function(response) {
        // Continuously update stream with data
        var body = '';
        response.on('data', function(d) {
            body += d;
        });
        response.on('end', function() {
            done(item, body);
        });
    });
}

